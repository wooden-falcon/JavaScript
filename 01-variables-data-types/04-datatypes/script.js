// There are essentially two datatypes in JS
// Primitive Data Types
// String: Sequence of characters. Must be in quotes or backticks
// Number: Integers as well as floating-point numbers
// Boolean: Logical entity/ true or false
// Null: Intentional absence of any object value
// undefined: Avariable dat has not yet been defines
// Symbol: built-in object whose constructor returns a uniques symbol
// BigInt: Numbers that are greater than the "Number" type can handle
// Reference Data Types
// Object literals, arrays and functions are all example of reference types

// String
const firstName = 'Sara';

// Number
const age = 30;
const temp = 98.9;

// Boolean
const hasKids = true;

// Null
const aptNumber = null;

// Undefined
let score;

// Symbol
const id = Symbol('id');

// BigInt

const n = 235692659829689852979228203027420598621n;

const output = n;

